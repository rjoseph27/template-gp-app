/**
 * @enum FormMode
 * @description The type of the form mode
 */
export enum FormMode {
    /**
     * @description The mode for creating a new item
     */
    CREATE = 'create',

    /**
     * @description The mode for editing an existing item
     */
    EDIT = 'edit',

    /**
     * @description The mode for viewing an existing item
     */
    VIEW = 'view'
}